	function flexContainerChange() {
			let inputFlexDirection = document.getElementById("flex-direction-id").value;
			let inputFlexWrap = document.getElementById("flex-wrap-id").value;
			let inputJustifyContent = document.getElementById("justify-content-id").value;
			let inputAlignItems = document.getElementById("align-items-id").value;
			let inputAlignContent = document.getElementById("align-content-id").value;
			document.getElementById("flex-container").style.flexDirection = inputFlexDirection;
			document.getElementById("flex-container").style.flexWrap = inputFlexWrap;
			document.getElementById("flex-container").style.justifyContent = inputJustifyContent;
			document.getElementById("flex-container").style.alignItems = inputAlignItems;
			document.getElementById("flex-container").style.alignContent = inputAlignContent;
			}
			
